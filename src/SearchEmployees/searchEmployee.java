    public List<String> searchEmployees(String searchTerm, String searchCriteria) {
        List<String> results = new ArrayList<>();
        try (BufferedReader reader = new BufferedReader(new FileReader(FILE_PATH))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] fields = line.split(",");
                String empID = fields[0].trim(); // Assuming EmpID is at index 0
                String departmentName = fields[5].trim(); 
                String name = fields[2].trim(); 
                String designationName = fields[4].trim(); 

                // Check if the search criteria matches the search term
                if (searchTerm.equalsIgnoreCase(empID) || searchTerm.equalsIgnoreCase(fields[1].trim())) {
                    results.add(line);
                }
                else if(searchTerm.equalsIgnoreCase(fields[5].trim())){
                    results.add(line);
                }
                else if(searchTerm.equalsIgnoreCase(fields[2].trim())){
                    results.add(line);
                }
                else if(searchTerm.equalsIgnoreCase(fields[4].trim())){
                    results.add(line);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return results;
    }
