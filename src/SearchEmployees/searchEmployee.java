    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

    // Get the search term from the input field (assuming txtSearchEmpID is the text field for entering EmpID or Epf No)
    String searchTerm = txtSearchEmpID.getText().trim();

    // Check if the search term is empty
    if (searchTerm.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter Employee ID or EPF No", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Create an instance of EmployeeSearch
    EmployeeSearch employeeSearch = new EmployeeSearch();

    // Call the searchEmployees method to get the search results
    List<String> results = employeeSearch.searchEmployees(searchTerm, "");

    // Check if the results are empty
    if (results.isEmpty()) {
        JOptionPane.showMessageDialog(null, "No employee found with the provided ID or EPF No", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Update the searchResultsTextArea with the search results
    StringBuilder resultText = new StringBuilder();
    resultText.append("Employee ID \t EPF No \t Name \t Age \t Designation \t Department\n");
    resultText.append("-------------------------------------------------------------------------------------------------------------------------\n");
    resultText.append("\n");

    for (String result : results) {
        // Split the result by commas to extract individual fields
        String[] fields = result.split(",");

        // Append each field, separated by tabs
        resultText.append(fields[0]).append("\t");
        resultText.append(fields[1]).append("\t");
        resultText.append(fields[2]).append("\t");
        resultText.append(fields[3]).append("\t");
        resultText.append(fields[4]).append("\t");
        resultText.append(fields[5]).append("\n");
    }

    // Set the text of the searchResultsTextArea
    searchResultsTextArea.setText(resultText.toString());

    }//GEN-LAST:event_btnSearchActionPerformed

